import { model, Schema } from "mongoose";

// Define the schema for the "Post" model
const PostSchema = new Schema(
  {
    userId: {
      type: String,
      required: true,
    },
    firstName: {
      type: String,
      required: true,
    },
    lastName: {
      type: String,
      required: true,
    },
    location: String,
    description: String,
    picturePath: String,
    userPicturePath: String,
    likes: {
      type: Map,
      of: Boolean,
    },
    comments: {
      type: Array,
      default: [],
    },
  },
  { timestamps: true }
);

// Create the "Post" model using the schema
const Post = model("Post", PostSchema);

// Export the "Post" model
export default Post;
